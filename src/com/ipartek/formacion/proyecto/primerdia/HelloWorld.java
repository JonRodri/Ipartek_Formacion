package com.ipartek.formacion.proyecto.primerdia;

public class HelloWorld {
/* Comentario de bloque
 * Toda CLASE en Java empieza por mayus.
 * No usar espacios en blanco.
 * Empezar siempre por letra.
 * Nomenclatura CamelCase ej.: NombreDeClase;
 * 
 * Nombres incorrectos: nombreDeClase, Nombre De Clase, 1Clase...
 */
	public static void main(String[] args) {
		/* VARIABLES:
		 * Siempre empiezan por minus.
		 * Tipo variable + nombre = valor;
		 * 1º Tipo variable => String
		 * 2º nombre variable => alumno, edad, ...
		 * 3º Asignar valor a la variable usando "="
		 * 4º Terminar sentencia con ;
		 */
		String alumn = "Jon";
		String apellido = "Rodriguez";

		System.out.println(" Hello " + alumn + " " + apellido);
	}//end main

}//end clase
